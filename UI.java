    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
          Class.forName("com.mysql.jdbc.Driver");
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost/crud","root","");
          String regno=regNo.getText().toString();
          String fname=fn.getText();
          String lname=ln.getText();
          String phone=pn.getText().toString();
          String insert="insert into data values(?,?,?,?,?)";
          if(regno.length()==0 || fname.length()==0 || lname.length()==0 || phone.length()==0){
              JOptionPane.showMessageDialog(this, "All fields are required", "Error", JOptionPane.ERROR_MESSAGE);
          }
          else{
          PreparedStatement stmt=con.prepareStatement(insert);
          stmt.setInt(1, 0);
          stmt.setString(2, regno);
          stmt.setString(3, fname);
          stmt.setString(4, lname);
          stmt.setString(5, phone);
          int s=stmt.executeUpdate();
          if(s>0)
          {
             DefaultTableModel tm=(DefaultTableModel)table.getModel(); 
             tm.setRowCount(0);
             show_Data();
           JOptionPane.showMessageDialog(this, "Data added succesfully", "success", JOptionPane.INFORMATION_MESSAGE);
          }}
         
          con.close();
        }
        catch(Exception e)
        {
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

